package com.shivshambhuwebapi.master.model;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Table;

@Entity
@Table(name = "bank_detail")
public class BankDetail {

	@Id
	@GeneratedValue(strategy = GenerationType.AUTO)
	@Column(name = "bank_det_id")
	private int bankDetId;

	@Column(name = "company_id")
	private int companyId;

	@Column(name = "bank_name")
	private String bankName;

	@Column(name = "bank_ifsc")
	private String bankIfsc;

	@Column(name = "bank_address")
	private String bankAddress;

	@Column(name = "acc_type")
	private int accType;

	@Column(name = "acc_no")
	private String accNo;

	@Column(name = "time_stamp")
	private String timeStamp;

	@Column(name = "del_status")
	private int delStatus;

	@Column(name = "is_used")
	private int isUsed;

	@Column(name = "ex_int1")
	private int exInt1;

	@Column(name = "ex_int2")
	private int exInt2;

	@Column(name = "ex_var1")
	private String exVar1;

	@Column(name = "ex_var2")
	private String exVar2;

	public int getBankDetId() {
		return bankDetId;
	}

	public void setBankDetId(int bankDetId) {
		this.bankDetId = bankDetId;
	}

	public int getCompanyId() {
		return companyId;
	}

	public void setCompanyId(int companyId) {
		this.companyId = companyId;
	}

	public String getBankName() {
		return bankName;
	}

	public void setBankName(String bankName) {
		this.bankName = bankName;
	}

	public String getBankIfsc() {
		return bankIfsc;
	}

	public void setBankIfsc(String bankIfsc) {
		this.bankIfsc = bankIfsc;
	}

	public String getBankAddress() {
		return bankAddress;
	}

	public void setBankAddress(String bankAddress) {
		this.bankAddress = bankAddress;
	}

	public int getAccType() {
		return accType;
	}

	public void setAccType(int accType) {
		this.accType = accType;
	}

	public String getAccNo() {
		return accNo;
	}

	public void setAccNo(String accNo) {
		this.accNo = accNo;
	}

	public String getTimeStamp() {
		return timeStamp;
	}

	public void setTimeStamp(String timeStamp) {
		this.timeStamp = timeStamp;
	}

	public int getDelStatus() {
		return delStatus;
	}

	public void setDelStatus(int delStatus) {
		this.delStatus = delStatus;
	}

	public int getIsUsed() {
		return isUsed;
	}

	public void setIsUsed(int isUsed) {
		this.isUsed = isUsed;
	}

	public int getExInt1() {
		return exInt1;
	}

	public void setExInt1(int exInt1) {
		this.exInt1 = exInt1;
	}

	public int getExInt2() {
		return exInt2;
	}

	public void setExInt2(int exInt2) {
		this.exInt2 = exInt2;
	}

	public String getExVar1() {
		return exVar1;
	}

	public void setExVar1(String exVar1) {
		this.exVar1 = exVar1;
	}

	public String getExVar2() {
		return exVar2;
	}

	public void setExVar2(String exVar2) {
		this.exVar2 = exVar2;
	}

	@Override
	public String toString() {
		return "BankDetail [bankDetId=" + bankDetId + ", companyId=" + companyId + ", bankName=" + bankName
				+ ", bankIfsc=" + bankIfsc + ", bankAddress=" + bankAddress + ", accType=" + accType + ", accNo="
				+ accNo + ", timeStamp=" + timeStamp + ", delStatus=" + delStatus + ", isUsed=" + isUsed + ", exInt1="
				+ exInt1 + ", exInt2=" + exInt2 + ", exVar1=" + exVar1 + ", exVar2=" + exVar2 + "]";
	}

}
